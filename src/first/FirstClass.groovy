package first

class FirstClass {
    private int a;
    private int b;

    FirstClass(int a, int b) {
        this.a = a
        this.b = b
    }

    int getA() {
        return a
    }

    void setA(int a) {
        this.a = a
    }

    int getB() {
        return b
    }

    void setB(int b) {
        this.b = b
    }
}
